# Define a function to encode text into Dancing Men cipher
def encode_dancing_men(text):
    encoded_text = ""
    for char in text.upper():
        if char.isalpha():
            encoded_text += dancing_men[char] + " "
        else:
            encoded_text += char
    return encoded_text

